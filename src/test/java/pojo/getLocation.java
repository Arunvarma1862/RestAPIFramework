package pojo;

public class getLocation {
	private double lat;                 // alt+ shift+ s = getter and setter values
	private double lng;
	
	
    public double getLat() {
		return lat;
	}
	public void setLat(double lat) {
		this.lat = lat;
	}
	public double getLng() {
		return lng;
	}
	public void setLng(double lng) {
		this.lng = lng;
	}
	
    
    
}
